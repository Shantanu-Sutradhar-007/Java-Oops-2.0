public class Ch04_LogicalOperator_02 {
    public static void main(String[] args) {

        int a = 10;
        if (a < 12 && a > 8) {
            System.out.println("Yes");

        }

        if (a < 12 || a > 55) {
            System.out.println("Yes");

        }

        if (a != 8) {
            System.out.println("Yes");

        }

    }
}